namespace CommandExample;

/// <summary>
/// The Receiver class contains the actual implementation of operations that can be performed.
/// </summary>
public class Receiver
{
    /// <summary>
    /// Closes a file.
    /// </summary>
    public void CloseFile()
    {
        // Implementation for closing a file
        Console.WriteLine("Closing the file...");
    }

    /// <summary>
    /// Opens a file.
    /// </summary>
    public void OpenFile()
    {
        // Implementation for opening a file
        Console.WriteLine("Opening the file...");
    }

    /// <summary>
    /// Saves a file.
    /// </summary>
    public void SaveFile()
    {
        // Implementation for saving a file
        Console.WriteLine("Saving the file...");
    }
}